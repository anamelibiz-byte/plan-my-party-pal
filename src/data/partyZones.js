// Returns themed food name if available, otherwise returns original task
export function getThemedFoodName(task, theme) {
  if (!theme) return task;
  const lower = theme.toLowerCase();
  // Check each theme keyword
  for (const [key, names] of Object.entries(THEMED_FOOD_NAMES)) {
    if (lower.includes(key)) {
      return names[task] || task;
    }
  }
  return task;
}

