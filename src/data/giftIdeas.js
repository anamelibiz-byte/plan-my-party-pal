// Helper function to determine age group from numeric age
export const getAgeGroup = (age) => {
  const numAge = parseInt(age);

  if (isNaN(numAge)) return 'youngKids'; // default

  if (numAge >= 1 && numAge <= 3) return 'toddlers';
  if (numAge >= 4 && numAge <= 7) return 'youngKids';
  if (numAge >= 8 && numAge <= 12) return 'olderKids';
  if (numAge >= 13) return 'teens';

  return 'youngKids'; // default fallback
};

